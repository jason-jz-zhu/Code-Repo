    def wiggleSort(self, nums):
        """
        :type nums: List[int]
        :rtype: void Do not return anything, modify nums in-place instead.
        """
        if nums is None or len(nums) == 0:
            return

        nums.sort()

        for i in xrange(2, len(nums), 2):
            nums[i], nums[i - 1] = nums[i - 1], nums[i]


